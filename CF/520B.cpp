int n, m;

// 1 ≤ n, m ≤ 10^4
map<ii, int> memo;

int dp(int n, int m) {
    if (n >= m) return n - m;
    if (memo.count({n, m})) return memo[{n, m}];
    int ans = 1 + dp(2 * n, m);
    if (m % 2 == 0) ans = min(ans, 1 + dp(n, m / 2));
    else ans = min(ans, 2 + dp(n, (m + 1) / 2));
    return memo[{n, m}] = ans;
}

