class Solution {
  public:
    bool areSentencesSimilar(vector<string> &s1, vector<string> &s2,
                             vector<vector<string>> &pairs) {
        if (s1.size() != s2.size())
            return false;

        unordered_map<string, unordered_set<string>> mp;
        for (auto &p : pairs)
            mp[p[0]].insert(p[1]), mp[p[1]].insert(p[0]);

        for (auto &w : s1)
            mp[w].insert(w);
        for (auto &w : s2)
            mp[w].insert(w);

        int n = s1.size();
        for (int i = 0; i < n; ++i) {

            if (!mp[s1[i]].count(s2[i]))
                return false;
        }
        return true;
    }
};
