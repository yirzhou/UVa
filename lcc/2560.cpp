class Solution {
  public:
    int f(vector<int> &nums, int k, int target) {
        vector<int> S;
        for (int i = 0; i < nums.size(); ++i) {
            if (nums[i] <= target && !S.back() != i - 1) S.pb(i);
            if (S.size() == k) return 1;
        }
        return 0;
    }

    int minCapability(vector<int> &nums, int k) {
        int lo = 1, hi = *max_element(ALL(nums));
        hi++;

        while (lo < hi) {
            int mid = lo + (hi - lo) / 2;
            if (f(nums, k, mid)) hi = mid;
            else lo = mid + 1;
        }

        return lo;
    }
};
