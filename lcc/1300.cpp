/*
Input: arr = [4,9,3], target = 10
Output: 3
Explanation: When using 3 arr converts to [3, 3, 3] which sums 9 and that's the
optimal answer.
*/
class Solution {
  public:
    int f(vi &nums, int target) {
        int sum = 0;
        for (auto &num : nums) sum += min(target, num);
        return sum;
    }

    int findBestValue(vector<int> &arr, int target) {
        int sum = 0;
        for (auto &num : arr) sum += num;
        if (sum < target) return *max_element(ALL(arr));
        int lo = 0, hi = 1e5 + 1;
        while (lo < hi) {
            int mid = (lo + hi) / 2;
            int res = f(arr, mid);
            if (res < target) lo = mid + 1;
            else hi = mid;
        }
        int r1 = f(arr, lo - 1), r2 = f(arr, lo);
        if (abs(r1 - target) <= abs(r2 - target)) return lo - 1;
        return lo;
    }
};
